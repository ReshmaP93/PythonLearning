#Relational/Comparison Operators
# > < >= <= == !=

#Relational Operators always returns a boolean values


a=5
b=10

print(a>b) #False
print(a<b) #True
print(a==b) #False
print(a!=b) #True
print(a<=b) #True
print(a>=b) #False


#Logical Operators
# and or not
#Always returns a boolean value

a=True
b=False


print(a and b) #False

print(a or b) #True

print(not a) #False